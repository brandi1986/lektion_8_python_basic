mood = "nervous"

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Why so sad?")
elif mood == "excited":
    print("Alright")
elif mood == "relaxed":
    print("Great, that you are relaxed")
else:
    print("I don't recognize this mood")
